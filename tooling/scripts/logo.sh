"
w=$(($(expr index "$src" "█") - 3));

render() {
  start="$1"
  jmp=$(($start + $w + 8));
  len=$(($w + $w - $jmp + 8));
  clear
  while IFS= read -r line; do
    echo "${line:0:start}${line:$w:3}${line:jmp:len}"
  done <<< "$src"
  sleep 0.02
}

if [[ $1 =~ ^[0-9]+$ ]]; then
  render $1
else
  while true; do
    for i in $(seq 0 $(($w-1))); do
      render $i
    done
    for i in $(seq $w -1 0); do
      render $i
    done
  done

fi
